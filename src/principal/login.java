    public boolean ifExists(){
        String usuario=txtnombreR.getText();
        String email=txtemail.getText();
        String contraseña=txtcontraseña.getText();
        
        if(usuario.equals("Escribe tu nombre.")||email.equals("Escribe tu correo.")||contraseña.equals("Escribe una contraseña.")){
            JOptionPane.showMessageDialog(null, "Por favor, inserte datos válidos y/o rellene todos los campos");
        }else{
        try{
        String sql = "SELECT usuario FROM usuarios WHERE usuario= '"+usuario+"'";
                PreparedStatement ps=cn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
        if(rs.next()){
            JOptionPane.showMessageDialog(null, "Este usuario ya está registrado");
            return true;
        } else{
            String sentencia = "SELECT email FROM usuarios WHERE email= '"+email+"'";
                ps=cn.prepareStatement(sentencia);
                rs = ps.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Este correo ya está registrado");
                    return true;
                } else{
                    return false;
                }
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Hubo un error inténtelo de nuevo más tarde");
        }
        } return true;
    }
    
