    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String usuario = txtnombrei.getText();
        String contraseña = txtcontrai.getText();
        
        if(usuario.isEmpty()||contraseña.isEmpty()||usuario.equals("Escribe tu nombre de usuario.")||contraseña.equals("Escribe tu contraseña")){
            JOptionPane.showMessageDialog(null, "Por favor, inserte datos válidos y/o rellene todos los campos");
        } else {
            try{
                String sql = "SELECT * FROM usuarios WHERE usuario= '"+usuario+"' AND clave ='"+contraseña+"'";
                PreparedStatement ps=cn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                if(rs.next()){
                    int IdDevuelto = rs.getInt(1);
                    JOptionPane.showMessageDialog(null, " Bienvenido "+usuario);
                    this.setVisible(false);
                    new menu(IdDevuelto).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Usuario no encontrado o no registrado");
                }
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Hubo un error inténtelo de nuevo más tarde");
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

